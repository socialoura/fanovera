function localizeFromFrench(locale: Exclude<SupportedLocale, "fr" | "en" | "es">): YouTubeCopy {
  const translated = deepTranslateCopy(copy.fr, locale);
  const walk = (value: unknown): unknown => {
    if (typeof value === "string") {
      return pageReplacements[locale].reduce((text, [from, to]) => text.replaceAll(from, to), value);
    }
    if (Array.isArray(value)) return value.map(walk);
    if (value && typeof value === "object") return Object.fromEntries(Object.entries(value).map(([key, item]) => [key, walk(item)]));
    return value;
  };
  return walk(translated) as YouTubeCopy;
}

